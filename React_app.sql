    -- Перевіряємо, чи статус змінюється на 'finished' або вже 'finished'
    IF NEW.status = 'finished' AND OLD.status = 'finished' THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Статус вже було змінено на finished. Зміна не дозволена.';
    END IF;
END
$$
