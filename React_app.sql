DELIMITER $$
CREATE TRIGGER `update_stake_after_match_finished` AFTER UPDATE ON `matches` FOR EACH ROW BEGIN
    -- Перевіряємо, чи статус змінився на 'finished'
    IF OLD.status IN ('pending', 'ongoing') AND NEW.status = 'finished' THEN
        -- Оновлюємо статус ставок в залежності від результатів матчу
        IF NEW.Team1Score > NEW.Team2Score THEN
            UPDATE stake
            SET status = 'won'
            WHERE match_id = NEW.MatchID AND team_id = NEW.Team1ID;

            UPDATE stake
            SET status = 'lost'
            WHERE match_id = NEW.MatchID AND team_id = NEW.Team2ID;
        ELSEIF NEW.Team1Score < NEW.Team2Score THEN
            UPDATE stake
            SET status = 'won'
            WHERE match_id = NEW.MatchID AND team_id = NEW.Team2ID;

            UPDATE stake
            SET status = 'lost'
            WHERE match_id = NEW.MatchID AND team_id = NEW.Team1ID;
        ELSE
            -- нічия, оновлчєм статус ставок на 'lost'
            UPDATE stake
            SET status = 'lost'
            WHERE match_id = NEW.MatchID;
        END IF;
    END IF;
END
$$
