CREATE TRIGGER `update_balance_and_add_notification` AFTER UPDATE ON `stake` FOR EACH ROW BEGIN
  --   статус з 'pending' на 'won'
  IF OLD.status = 'pending' AND NEW.status = 'won' THEN
    -- Оновлюємо баланс користувача при виграші
    UPDATE users
    SET bonus_money = bonus_money + (NEW.amount * NEW.Coef)
    WHERE id = NEW.user_id;

